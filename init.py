import sys
import time
import os
from colorama import Fore

def loading_bar():
    bar_length = 15 
    for i in range(bar_length + 1):
        percent = int((i / bar_length) * 100)
        bar = "|" + "=" * i + "-" * (bar_length - i) + "|"
        sys.stdout.write(f"\r{bar} {percent}%")
        sys.stdout.flush()
        time.sleep(0.1)  
    print() 

